"use client";

import { Button, Typography } from "@/ui/components";
import { Icon, LatLngExpression } from "leaflet";
import { useEffect, useMemo, useRef, useState } from "react";
import { Marker, Popup } from "react-leaflet";
import { Marker as LeafletMarker, PointExpression } from "leaflet";
import { Stop } from "@/app/lib/definitions";

interface StopMarkerProps {
  stop: Stop;
  index?: number;
  initialPosition: LatLngExpression;
  label?: string;
  draggable: boolean;
  onEditStop: (stop: Stop) => void;
  onMarkerMoved: (stop: Stop) => void;
  onDeleteStop: (stop: Stop) => void;
}

function StopMarker({
  stop,
  index,
  initialPosition,
  label,
  draggable,
  onEditStop,
  onMarkerMoved,
  onDeleteStop,
}: StopMarkerProps) {

  const stopIcon = new Icon({
    iconUrl: "/icons/bus-stop.svg",
    iconSize: [20, 20],
    iconAnchor: [10, 10],
  });

  const markerRef = useRef<LeafletMarker | null>(null);
  const [position, setPosition] = useState(initialPosition);

  useEffect(() => {
    setPosition(initialPosition);
  }, [initialPosition]);

  const eventHandlers = useMemo(
    () => ({
      dragend() {
        const marker = markerRef.current;
        if (marker != null) {
          setPosition(marker.getLatLng());
          let newStop = { ...stop, position: marker.getLatLng() };
          onMarkerMoved(newStop);
        }
      },
    }),
    [onMarkerMoved],
  );

  return (
    <>
      <Marker
        draggable={draggable}
        eventHandlers={eventHandlers}
        key={index}
        ref={markerRef}
        icon={stopIcon}
        opacity={draggable ? 0.5 : 1 }
        position={position}
        autoPan={true}
      >
        {label && (
          <Popup>
            <StopPopup
              stop={stop}
              onEditStop={onEditStop}
              onDeleteStop={onDeleteStop}
            />
          </Popup>
        )}
      </Marker>
    </>
  );
}

function StopPopup({
  stop,
  onEditStop,
  onDeleteStop,
}: {
  stop: Stop;
  onEditStop: (stop: Stop) => void;
  onDeleteStop: (stop: Stop) => void;
}) {
  return (
    <div>
      <Typography variant="note">{stop.name}</Typography>
      <Typography variant="note">{stop.description}</Typography>
      <div>
        <Button
          trailIconUrl="/icons/edit.svg"
          label="Editar"
          size="small"
          onClick={() => onEditStop(stop)}
        />
        <Button
          trailIconUrl="/icons/delete.svg"
          label="Eliminar"
          size="small"
          primary={false}
          onClick={() => onDeleteStop(stop)}
        />
      </div>
    </div>
  );
}

export default StopMarker;
